
def get_person_info(first_name: str,
                    last_name: str,
                    phone_number: str,
                    address: str) -> str:
    
    first_name = first_name
    last_name = last_name
    phone_number = phone_number
    address = address

    return  first_name + '; ' + last_name + '; ' + phone_number + '; ' + address
    